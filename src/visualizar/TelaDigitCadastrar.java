    /**
     * Pega os dados da ficha (id, nome, nascimento, sexo, contato, cpf, rg,
     * endere√ßo) e coloca formatado, pro banco, em um objeto Digitadores
     *
     * @param id
     * @param jNome
     * @param jNascimento
     * @param jSexoM
     * @param jContato
     * @param jRg
     * @param jCpf
     * @param jEndereco
     * @return
     */
    public static Digitadores getFormatedDigitadores(int id, JTextField jNome, JFormattedTextField jNascimento, JCheckBox jSexoM,
            JFormattedTextField jContato, JTextField jRg, JFormattedTextField jCpf, JTextField jEndereco) {
        Digitadores digi = new Digitadores();
        String newNasc = jNascimento.getText();
        String newCpf = jCpf.getText();
        String newContato = jContato.getText();

        digi.setNome(jNome.getText().trim());
        digi.setNascimento(newNasc.substring(6, 10) + "-" + newNasc.substring(3, 5) + "-" + newNasc.substring(0, 2));   // modificar a data para o formato do banco

        digi.setSexo(jSexoM.isSelected() ? "M" : "F");

        digi.setContato(newContato.substring(1, 3) + newContato.substring(5, 10) + newContato.substring(11, 15));   // retirar a formata√ß√£o da m√°scara pro banco
        digi.setRg(jRg.getText());
        digi.setCpf(newCpf.substring(0, 3) + newCpf.substring(4, 7) + newCpf.substring(8, 11) + newCpf.substring(12, 14));
        digi.setEndereco(jEndereco.getText().trim());
        digi.setId(id);
        return digi;
    }

    /**
     * Verifica se tudo que foi digitado nas caixas de texto est√° nos conformes
     * do banco
     *
     * @param painel
     * @param jNome
     * @param jNascimento
     * @param jSexoM
     * @param jSexoF
     * @param jRg
     * @param jCpf
     * @param jEndereco
     * @return
     */
    public static boolean checkCaixas(JPanel painel, JTextField jNome, JFormattedTextField jNascimento, JCheckBox jSexoM, JCheckBox jSexoF,
            JTextField jRg, JFormattedTextField jCpf, JTextField jEndereco) {
        String rg = jRg.getText().trim();
        String txtnome = jNome.getText().trim();
        String endereco = jEndereco.getText().trim();

        if (txtnome.equals("")) {
            JOptionPane.showMessageDialog(painel, "O Nome do paciente deve ser preenchido.", "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        } else if (txtnome.matches("\\D+") == false) {     //verificar se o txtnome √© somente letras
            JOptionPane.showMessageDialog(painel, "O campo Nome n√£o pode conter n√∫meros ou s√≠mbolo.", "Erro",
                    JOptionPane.ERROR_MESSAGE);
        } else if (txtnome.equals("Admin")) {
            JOptionPane.showMessageDialog(painel, "O campo Nome n√£o pode ser este", "Erro",
                    JOptionPane.ERROR_MESSAGE);
        } else if (txtnome.length() > 45) {
            JOptionPane.showMessageDialog(painel, "O Nome do digitador n√£o pode conter muitos caracteres.", "Erro",
                    JOptionPane.ERROR_MESSAGE);
        } else if (jNascimento.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(painel, "A data de Nascimento do digitador deve ser preenchida.", "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        } else if (jSexoM.isSelected() == false && jSexoF.isSelected() == false) {
            JOptionPane.showMessageDialog(painel, "√â necess√°rio informar o Sexo do digitador.", "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        } else if (rg.equals("")) {
            JOptionPane.showMessageDialog(painel, "O RG do digitador deve ser preenchido.", "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        } else if (rg.length() > 13) {
            JOptionPane.showMessageDialog(painel, "O RG do digitador deve conter no m√°ximo 13 n√∫meros.", "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        } else if (rg.matches("\\d+") == false) {   //verifica se o rg √© somente n√∫meros
            JOptionPane.showMessageDialog(painel, "O campo RG s√≥ pode conter n√∫meros.", "Erro",
                    JOptionPane.ERROR_MESSAGE);
        } else if (jCpf.getText().equals("   .   .   -  ")) {
            JOptionPane.showMessageDialog(painel, "O CPF do  digitador deve ser preenchido.", "Erro",
                    JOptionPane.ERROR_MESSAGE);
        } else if (endereco.length() > 75) {
            JOptionPane.showMessageDialog(painel, "O Endere√ßo n√£o pode ser maior que 75 caracteres.", "Aviso",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            return true;
        }

        return false;
    }

    private void jCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCadastrarActionPerformed
        // TODO add your handling code here:
        DigitadoresBanco db = new DigitadoresBanco();
        if (jCadastrar.getText().equals("Cadastrar")) {     //Se o nome for Cadastrar - Cadastra o Digitador
            if (checkCaixas(jPanel5, jNome, jNascimento, jSexoM, jSexoF, jRg, jCpf, jEndereco)) {
                //(id = -1) pois n√£o h√° id ainda
                digitador = getFormatedDigitadores(-1, jNome, jNascimento, jSexoM, jContato, jRg, jCpf, jEndereco);
                if (db.validarCpfRg(digitador.getId(), digitador.getCpf(), digitador.getRg())) {

                    digitador.setLogin(digitador.getCpf());  //Ao cadastrar o login e senha do digitador ser√£o seu cpf
                    digitador.setSenha(digitador.getCpf());
                    db = new DigitadoresBanco();
                    if (db.inserir(digitador)) {
                        JOptionPane.showMessageDialog(this, "O Digitador foi cadastrado com sucesso!\n\nInforme ao "
                                + "Digitador que seu Login e Senha s√£o o CPF");
                        jNome.setText("");
                        jNascimento.setText("");
                        jSexoM.setSelected(false);
                        jSexoF.setSelected(false);
                        jContato.setText("");
                        jRg.setText("");
                        jCpf.setText("");
                        jEndereco.setText("");
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro ao cadastrar o Digitador. Tente novamente.", "Erro",
                                JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Os dados informados no Cpf ou Rg j√° existem.", "Aviso",
                            JOptionPane.WARNING_MESSAGE);
                }
            }
        } else {                //Caso n√£o se torna bot√£o Editar - some ao ser apertado
            jCadastrar.setVisible(false);
            jAtualizar.setVisible(true);
            jPin.setVisible(true);
            jNomePin.setVisible(true);

            if (!("Admin").equals(jNome.getText())) {  // O nome do Admin n√£o pode ser modificado
                jNome.setEnabled(true);
            }
            jNascimento.setEnabled(true);
            jSexoM.setEnabled(true);
            jSexoF.setEnabled(true);
            jContato.setEnabled(true);
            jRg.setEnabled(true);
            jCpf.setEnabled(true);
            jEndereco.setEnabled(true);
        }
    }//GEN-LAST:event_jCadastrarActionPerformed

    private void jAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAtualizarActionPerformed
        // TODO add your handling code here:
        DigitadoresBanco db = new DigitadoresBanco();
        int escolha;

        //Ao apertar o botao editar este botao atualizar aparece. Apertando este
        //bot√£o, se tudo tiver certo, aparecer√° uma mensagem de confirma√ß√£o e
        //ap√≥s ocorrer√° valida√ß√£o do pin colocado
        if (checkCaixas(jPanel5, jNome, jNascimento, jSexoM, jSexoF, jRg, jCpf, jEndereco)) {
            escolha = JOptionPane.showConfirmDialog(null, "Deseja realmente modificar os seus dados?",
                    "Modificar dados", 0, JOptionPane.QUESTION_MESSAGE);

            if (escolha == 0) {  // confirma√ß√£o do usuario
                if (digitador.getPin().equals(new String(jPin.getPassword()))) {
                    digitador = getFormatedDigitadores(idDigit, jNome, jNascimento, jSexoM, jContato, jRg, jCpf, jEndereco);

                    if (db.validarCpfRg(digitador.getId(), digitador.getCpf(), digitador.getRg())) {
                        db = new DigitadoresBanco();
                        if (db.atualizar(digitador)) {
                            JOptionPane.showMessageDialog(this, "Atualizado com sucesso!\n\nSe modificou o seu Nome, ele aparecer√° na aba Conta no seu pr√≥ximo Acesso.");
                            this.dispose();
                        } else {
                            JOptionPane.showMessageDialog(this, "Erro ao atualizar os seus dados. Tente novamente.", "Erro",
                                    JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Os dados informados no Cpf ou Rg j√° existem.", "Erro",
                                JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Pin Incorreto. Tente novamente.", "Erro",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jAtualizarActionPerformed

