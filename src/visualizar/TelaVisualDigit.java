    /**
     * Se confirmado atualiza os dados do digitador
     */
    private void atualizarDigitador() {
        DigitadoresBanco db = new DigitadoresBanco();
        int escolha;

        if (TelaDigitCadastrar.checkCaixas(jPanel5, jNome, jNascimento, jSexoM, jSexoF, jRg, jCpf, jEndereco)) {
            escolha = JOptionPane.showConfirmDialog(this, "Deseja realmente modificar os dados do digitador?",
                    "Modificar dados", 0, JOptionPane.QUESTION_MESSAGE);

            if (escolha == 0) {  // confirma√ß√£o do usuario
                digitador = TelaDigitCadastrar.getFormatedDigitadores(digitador.getId(), jNome, jNascimento, jSexoM,
                        jContato, jRg, jCpf, jEndereco);

                if (db.validarCpfRg(digitador.getId(), digitador.getCpf(), digitador.getRg())) {
                    db = new DigitadoresBanco();
                    if (db.atualizar(digitador)) {
                        JOptionPane.showMessageDialog(this, "Digitador atualizado com sucesso.");
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro ao atualizar os dados do digitador. Tente novamente.", "Erro",
                                JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Os dados informados no Cpf ou Rg j√° existem.", "Erro",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }

